package day_03_escape_sequence;

public class Quotes {
    public static void main(String[] args) {
        //the backward slash \ is an escape sequence
        // the backward slash \t stand for tab in outline instead of 4 spaces
        // the backward  slash \n is to start new line in out
        System.out.println("\"every\\\\one love \"java\" programming\"");




        System.out.println("\"every\\\\one love \"java\" programming\"");
    }
}
