package Day_17_Loop2;

public class DoWhileExample {
    public static void main(String[] args) {

        int n = 100;

        do {
            System.out.println("Do WHILE LOOP: "  + n);
            n++;
        }while (n <= 10);



        int m = 100;
        while (m <= 10){
            System.out.println("WHILE LOOP: " + m);
            m++;

        }

    }
}
